    public void DameTargeta(Boolean aux) {
        if (contador >= 1 && contador <= 6) {
            if (aux) {
                numeroPensado = numeroPensado + Clave[contador - 1];
                System.out.println("Clave :" + Clave[contador - 1] + "\n" + "sumamos numero: " + numeroPensado);
            } else {
                System.out.println("posicion de la clave: " + Clave[contador - 1] + "\n" + "numero pensado: " + numeroPensado);
            }
            if (contador == 6) {
                JOptionPane.showMessageDialog(rootPane, "El numero que pensaste es\n" + numeroPensado, "Adivine tu numero", WIDTH, null);
                btnSi.setEnabled(false);
                btnNo.setEnabled(false);
                contador = 5;//por que lo iguale a 5, por que en la linea 47 incrementa uno osea se vuelve 6 otra vez
            }
            contador = contador + 1;
            lblTarjeta.setText("Ficha " + contador);
            lblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource(Ficha + contador + ".png")));
        } else {
            JOptionPane.showMessageDialog(rootPane, "El contador esta fuera de rango", "Error", WIDTH, null);
            this.dispose();
        }
    }

