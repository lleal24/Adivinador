    private void btnReiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReiniciarActionPerformed
        if (bnd) {
            JOptionPane.showMessageDialog(rootPane, "Piensa un número del 1 al 60\n"
                    + "Una vez hecho esto busca tu numero \n"
                    + "en las tarjetas que se te mostraran y\n"
                    + "da click en el boton 'Si' o 'No' \n"
                    + "segun corresponda", "Iniciar el juego", WIDTH, null);
            JOptionPane.showMessageDialog(rootPane, "Presta atención a las tarjetas y busca tu número\n"
                    + "Ahora si empecemos", "Aviso", WIDTH, null);
            btnReiniciar.setText("Reiniciar");
            lblTarjeta.setText("Ficha 1");
            btnSi.setEnabled(true);
            btnNo.setEnabled(true);
            lblImagen.setIcon(new javax.swing.ImageIcon(getClass().getResource(Ficha + "1.png")));
            bnd = false;
        } else {
            int seleccion = JOptionPane.showOptionDialog(this, "¿Quieres Salir Reinicar El Juego?",
                    "Salir", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[]{"Si", "No"}, "Si");
            if (seleccion != -1) {
                if ((seleccion + 1) == 1) {
                    this.dispose();
                    VistaAdivinador vista = new VistaAdivinador();
                    vista.setVisible(true);
                } else {
                }
            }
        }
    }//GEN-LAST:event_btnReiniciarActionPerformed

